type Props = {
  filters: string[];
};

const Gallery: React.FC<Props> = ({ filters }) => {
  const filteredProjects = projects.filter((project) => {
    let k = false;
    project.tags.forEach((tag) => {
      if (filters.includes(tag)) {
        k = true;
      }
    });
    return k;
  });

  return (
    <GalleryWrapper>
      {filteredProjects.map(({ id, description, img, url, name }) => {
        return (
          <Card
            key={id}
            id={id}
            img={img}
            text={description}
            url={url}
            name={name}
          />
        );
      })}
    </GalleryWrapper>
  );
};

export default Gallery;
